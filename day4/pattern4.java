public class pattern4 {
    public static void main(String[] args) {
        int n = 4;

        // বাইরের লুপটি n থেকে শুরু হয়ে 1 পর্যন্ত চলবে
        // শর্তটি i >= 1 হতে হবে
        for (int i = n; i >= 1; i--) {
            // ভেতরের লুপটি 1 থেকে i পর্যন্ত চলবে
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            // একটি সারি প্রিন্ট করার পর নতুন লাইনে যাওয়ার জন্য
            System.out.println();
        }
    }
}

